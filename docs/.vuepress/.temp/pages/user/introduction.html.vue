<template><div><h1 id="introduction" tabindex="-1"><a class="header-anchor" href="#introduction"><span>Introduction</span></a></h1>
<p>The <strong>Cartographer App</strong> provides placement information of zones of interest in (historical) documents.<br>
The primary use case is the identification of bounding boxes of measures in music documents.</p>
<p>It is a successor of the Vertaktoid, but unlike that project, it uses <strong>web technology</strong> and is thus platform-independent.</p>
<p>It can optionally use the <strong>Measure Detector</strong> for automatic recognition of measure positions, while still allowing manual correction.</p>
